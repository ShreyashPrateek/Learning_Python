# DataTypes in Python
'''Python has several built-in data types to store different kinds of data.
Here are some of the most commonly used data types:

1. Integer (int): Represents whole numbers without decimal points.
Example: 42, -10, 0

2. Float (float): Represents numbers with decimal points.
Example: 3.14, -0.5, 2.0

3. String (str): Represents a sequence of characters enclosed in single or double quotes.
Example: "Hello, World!", 'Python', "42"

4. Boolean (bool): Represents a binary value, either True or False.
Example: True, False

5. NoneType (None): Represents the absence of a value or a null value.
Example: None
'''

# Example usage of data types
x = 42
print(type(x))  # Output: <class 'int'>

y = "Hello"
print(type(y))  # Output: <class 'str'>

z = 3.14
print(type(z))  # Output: <class 'float'>

a = None
print(type(a))  # Output: <class 'NoneType'>


# Print sum of two numbers
a = 10
b = 20
sum = a + b
print("Sum:", sum)  # Output: Sum: 30


# Print difference of two numbers
a = 10  
b = 5
difference = a - b
print("Difference:", difference)  # Output: Difference: 5