# Variables
# In Python, variables are used to store data values. 
# You don't need to declare the type of a variable explicitly; 
# Python infers it based on the value assigned.

name = "Shreyash"  # String variable
age = 21           # Integer variable
height = 5.8      # Float variable
is_student = True # Boolean variable

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)    
print("Is Student:", is_student)



# Rules for Identifiers
# 1. Must start with a letter (a-z, A-Z) or an underscore (_)
# 2. Can contain letters, digits (0-9), and underscores (_)
# 3. Cannot be a reserved keyword in Python
# 4. Case-sensitive (e.g., 'age' and 'Age' are different)

