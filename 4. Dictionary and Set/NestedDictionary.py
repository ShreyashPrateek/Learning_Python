# Nested Dictionary

# A dictionary can contain dictionaries, this is called nested dictionaries.

myfamily = {
  "child1" : {
    "name" : "Shreyash",
    "year" : 2000
  },
  "child2" : {
    "name" : "Prateek",
    "year" : 2001
  },
  "child3" : {
    "name" : "Shubham",
    "year" : 2002
  }
}

print(myfamily)