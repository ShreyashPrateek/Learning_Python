# Set in python
# Set is collection of unordered items
# Each element in the set must be unique and immutable
# Sets are mutable, meaning you can add or remove elements after creation
# Sets are defined by enclosing elements in curly braces {} or using the set() function

# Creating a set
my_set = {1, 2, 3, 4, 5}
